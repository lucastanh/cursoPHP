<?php

$a = 10;
$b = 8;


if ($a === $b) {
    echo "A equals B";
}

// Not Equal
if ($a !== $b) {
    echo "A Not Equal B";
}

// Greater then
if ($a > $b) {
    echo "A is greater then B";
}

// Less then
if ($a < $b) {
    echo "A is less then B";
}

// Greater then or equal to
if ($a >= $b) {
    echo "A is greater then or equal to B";
}

// Less then or equal to
if ($a <= $b) {
    echo "A is less then or equal to";
}
